import os


# ======================================================================================================================
# Functions
# ======================================================================================================================
def ps_check_pid(pid):
    """
    Check for the existence of a unix pid.
    """
    try:
        os.kill(pid, 0)
    except OSError:
        return False
    else:
        return True
