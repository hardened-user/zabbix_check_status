import os
import traceback

from .log import log


# ======================================================================================================================
# Functions
# ======================================================================================================================
def fs_rm_file(path):
    try:
        os.remove(path)
    except Exception as err:
        log.c("Exception :: {}\n{}".format(err, "".join(traceback.format_exc())))
        return False
    # __________________________________________________________________________
    log.d2("File deleted :: {}".format(path))
    return True
